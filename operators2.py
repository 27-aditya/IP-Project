# =: Simple assignment
# +=: Addition assignment
# -=: Subtraction assignment
# *=: Multiplication assignment
# /=: Division assignment
# %=: Modulus assignment
# **=: Exponentiation assignment
# //=: Floor division assignment
# ==: Equal to
# !=: Not equal to
# >: Greater than
# <: Less than
# >=: Greater than or equal to
# <=: Less than or equal to

x = 10
x += 5  # Equivalent to x = x + 5
print(x)  # 15

y = 20
y *= 2  # Equivalent to y = y * 2
print(y)  # 40

a = 5
b = 10

print(a == b)  # False
print(a != b)  # True
print(a < b)   # True
print(a >= b)  # False
